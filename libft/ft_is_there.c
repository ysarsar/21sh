#include "libft.h"

int		ft_is_there(char *s, char c)
{
	if (s)
	{
		while (*s)
		{
			if (*s == c)
				return (1);
			s++;
		}
		if (c == '\0' && *s == '\0')
			return (1);
		return (0);
	}
	return (0);
}
